	<h1 class="text-center display-4 text-muted">Help</h1>

	<h3 class="mt-5">How do I create a new account?</h3>
	<p class="lead text-muted">To create an account, click on the "Login" button in the top right corner, and then click on the "Create an
		account" button and fill in the signup form to create an account. It's FREE to register.</p>

	<h3 class="mt-5">How do I purchase tickets?</h3>
	<p class="lead text-muted">Start by logging into your account or signing up for a new account.
		<br>Click on the "Login" button in the top right corner and login into your account by entering your email
		address and password.
		<br>If you don't have an account, click on the "Create an account" button and fill in the signup form.

		<br>Once you are logged in, click on the "Buy Tickets" button for the event you are interested in, and fill in
		the ticket class and number of tickets.
		<br>Click on the "PayPal" button and log into your PayPal account to pay for the tickets. Once the payment is
		completed, click on the "Finish" button to place your order.
	</p>

	<h3 class="mt-5">How do I view my orders and purchased tickets?</h3>
	<p class="lead text-muted">Log into your account, click on the "My Account" button in the top right corner, and click on "Order History"
		button.
		From there you can view all your previous orders and the tickets in each order.
		Tickets can be downloaded by clicking on the download icon.
	</p>

	<h3 class="mt-5">How to become a VIP member?</h3>
	<p class="lead text-muted">When registering for a new account, you can choose between a free account or a VIP account. VIP accounts have an
		annual fee of $45 per year.
		If you have a free account and would like to upgrade to a VIP account, please contact us to
		have your account upgraded to VIP.
	</p>


	<h3 class="mt-5">I forgot my password</h3>
	<p class="lead text-muted">If you forgot your password, you can reset it by clicking on "Login" button and then on the "Reset Password" button.
		Enter your email address, and you will receive a reset code by email. Copy and paste the reset code into the
		next page and enter a new password. Click on "Change Password" to save the new password. You can now login with
		your new password.
	</p>


	<h3 class="mt-5">How do I update my details?</h3>
	<p class="lead text-muted">Log into your account, click on "My Account" -> "Manage Account" -> "Update My Details".</p>

	<h3 class="mt-5">How do I change my password?</h3>
	<p class="lead text-muted">Log into your account, click on "My Account" -> "Manage Account" -> "Change Password".</p>


	<h3 class="mt-5">What payment methods can I use?</h3>
	<p class="lead text-muted">We accept all credit cards and PayPal. Credit card payments are processed through PayPal.</p>


